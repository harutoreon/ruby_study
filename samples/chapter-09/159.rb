time = Time.new(1995, 12, 21, 17, 0, 0, '+09:00')
p time        #=> 1995-12-21 17:00:00 +0900
p time.year   #=> 1995
p time.month  #=> 12
p time.day    #=> 21
p time.hour   #=> 17
p time.min    #=> 0
p time.sec    #=> 0

p Time.new(2020)      #=> 2020-01-01 00:00:00 +0000
p Time.new(2020, 12)  #=> 2020-12-01 00:00:00 +0000

p Time.new(2004, 7, 24, 19, 43, 00, 'UTC')    #=> 2004-07-24 19:43:00 UTC
p Time.new(1998, 1, 16, 21, 13, 5, '+09:00')  #=> 1998-01-16 21:13:05 +0900
