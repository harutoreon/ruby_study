z = 3 + 4i
puts z                       #=> 3+4i
puts "Re(#{z}) = #{z.real}"  #=> Re(3+4i) = 3
puts "Im(#{z}) = #{z.imag}"  #=> Im(3+4i) = 4

z1 = 3 + 4i
z2 = 5 - 6i
puts "(#{z1} + (#{z2})) = #{z1 + z2}"  #=> (3+4i + (5-6i)) = 8-2i
puts "(#{z1} - (#{z2})) = #{z1 - z2}"  #=> (3+4i - (5-6i)) = -2+10i
puts "(#{z1} * (#{z2})) = #{z1 * z2}"  #=> (3+4i * (5-6i)) = 39+2i
puts "(#{z1} / (#{z2})) = #{z1 / z2}"  #=> (3+4i / (5-6i)) = -9/61+38/61i
puts "(#{z1})^2 = #{z1 ** 2}"          #=> (3+4i)^2 = -7+24i
